using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Runtime.Serialization;

namespace Tadmap.Model.Image
{
   [Serializable]
   public class BinaryRepositoryException : Exception
   {
      public BinaryRepositoryException()
         :base()
      {
      }

      public BinaryRepositoryException(string message)
         :base(message)
      {
      }

      protected BinaryRepositoryException(SerializationInfo info, StreamingContext context)
         :base(info, context)
      {
      }

      public BinaryRepositoryException(string message, Exception innerException)
         :base(message, innerException)
      {
      }
   }
}
